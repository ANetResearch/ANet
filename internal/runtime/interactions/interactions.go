// List returns interactions in the given role with seq > sinceSeq, oldest first. If status is non-empty
// it filters to that status (e.g. queued inbound = the provider's pending work). limit ≤ 0 → 100.
func (s *Store) List(role Role, status Status, sinceSeq int64, limit int) ([]*Interaction, error) {
	if limit <= 0 {
		limit = 100
	}
	q := `SELECT ` + ixColumns + ` FROM interaction WHERE role=? AND seq > ?`
	args := []any{string(role), sinceSeq}
	if status != "" {
		q += ` AND status=?`
		args = append(args, string(status))
	}
	q += ` ORDER BY seq LIMIT ?`
	args = append(args, limit)
	rows, err := s.db.Query(q, args...)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []*Interaction
	for rows.Next() {
		ix, err := scanRows(rows)
		if err != nil {
			return nil, err
		}
		out = append(out, ix)
	}
	return out, rows.Err()
}

type scanner interface{ Scan(dest ...any) error }

func scanOne(row scanner) (*Interaction, error) {
	ix, err := scanRows(row)
	if errors.Is(err, sql.ErrNoRows) {
		return nil, ErrNotFound
	}
	return ix, err
}

func scanRows(sc scanner) (*Interaction, error) {
	ix := &Interaction{}
	var role, status string
	if err := sc.Scan(&ix.Seq, &ix.ID, &role, &ix.PeerAID, &ix.Goal, &status,
		&ix.RequestCID, &ix.RequestDoc, &ix.ResultCID, &ix.Result, &ix.Receipt, &ix.Review,
		&ix.EndReqBy, &ix.EndAccBy, &ix.CreatedAt, &ix.UpdatedAt); err != nil {
		return nil, err
	}
	ix.Role = Role(role)
	ix.Status = Status(status)
	return ix, nil
}
