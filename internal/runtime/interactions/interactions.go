// ixColumns is the shared SELECT column list for scanning an Interaction (keep in sync with scanRows).
const ixColumns = `seq,id,role,peer_aid,goal,status,request_cid,request_doc,result_cid,result,receipt,review,end_req_by,end_acc_by,created_at,updated_at`

// Get returns one interaction by id.
func (s *Store) Get(id string) (*Interaction, error) {
	row := s.db.QueryRow(`SELECT `+ixColumns+` FROM interaction WHERE id=?`, id)
	return scanOne(row)
}

// AddMessage appends one entry to an interaction's conversation log and bumps the interaction's
// updated_at so it sorts to the top of the chat list. Returns the new message seq.
func (s *Store) AddMessage(interactionID, senderAID, kind, body string) (int64, error) {
	if interactionID == "" || senderAID == "" || kind == "" {
		return 0, fmt.Errorf("%w: interaction_id, sender and kind required", ErrBadInput)
	}
	s.mu.Lock()
	defer s.mu.Unlock()
	now := time.Now().UTC().Format(time.RFC3339Nano)
	res, err := s.db.Exec(
		`INSERT INTO message(interaction_id,sender_aid,kind,body,created_at) VALUES(?,?,?,?,?)`,
		interactionID, senderAID, kind, body, now)
	if err != nil {
		return 0, err
	}
	_, _ = s.db.Exec(`UPDATE interaction SET updated_at=? WHERE id=?`, now, interactionID)
	return res.LastInsertId()
}

// Messages returns an interaction's conversation log, oldest first.
func (s *Store) Messages(interactionID string) ([]Message, error) {
	rows, err := s.db.Query(
		`SELECT seq,interaction_id,sender_aid,kind,body,created_at FROM message WHERE interaction_id=? ORDER BY seq`,
		interactionID)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []Message
	for rows.Next() {
		var m Message
		if err := rows.Scan(&m.Seq, &m.InteractionID, &m.SenderAID, &m.Kind, &m.Body, &m.CreatedAt); err != nil {
			return nil, err
		}
		out = append(out, m)
	}
	return out, rows.Err()
}

// AddAttachment stores one binary attachment (with its bytes) against a message of an interaction.
func (s *Store) AddAttachment(interactionID string, msgSeq int64, a Attachment) error {
	if interactionID == "" || a.CID == "" {
		return fmt.Errorf("%w: interaction_id and attachment cid required", ErrBadInput)
	}
	s.mu.Lock()
	defer s.mu.Unlock()
	now := time.Now().UTC().Format(time.RFC3339Nano)
	_, err := s.db.Exec(
		`INSERT INTO attachment(interaction_id,msg_seq,name,mime,size,cid,data,created_at) VALUES(?,?,?,?,?,?,?,?)`,
		interactionID, msgSeq, a.Name, a.Mime, a.Size, a.CID, a.Data, now)
	return err
}

// Attachments returns the metadata (NO bytes) of all attachments in an interaction, oldest first.
func (s *Store) Attachments(interactionID string) ([]Attachment, error) {
	rows, err := s.db.Query(
		`SELECT seq,interaction_id,msg_seq,name,mime,size,cid,created_at FROM attachment WHERE interaction_id=? ORDER BY seq`,
		interactionID)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []Attachment
	for rows.Next() {
		var a Attachment
		if err := rows.Scan(&a.Seq, &a.InteractionID, &a.MsgSeq, &a.Name, &a.Mime, &a.Size, &a.CID, &a.CreatedAt); err != nil {
			return nil, err
		}
		out = append(out, a)
	}
	return out, rows.Err()
}

// AttachmentData returns one attachment WITH its bytes, by interaction + content CID.
func (s *Store) AttachmentData(interactionID, cid string) (*Attachment, error) {
	row := s.db.QueryRow(
		`SELECT seq,interaction_id,msg_seq,name,mime,size,cid,data,created_at FROM attachment WHERE interaction_id=? AND cid=? LIMIT 1`,
		interactionID, cid)
	var a Attachment
	err := row.Scan(&a.Seq, &a.InteractionID, &a.MsgSeq, &a.Name, &a.Mime, &a.Size, &a.CID, &a.Data, &a.CreatedAt)
	if errors.Is(err, sql.ErrNoRows) {
		return nil, ErrNotFound
	}
	if err != nil {
		return nil, err
	}
	return &a, nil
}

// SetEndRequested records that `by` proposed ending the interaction and moves it to StatusEnding (only
// from an active/ending state — a done/failed interaction is not reopened).
func (s *Store) SetEndRequested(id, by string) error {
	if id == "" || by == "" {
		return fmt.Errorf("%w: id and by required", ErrBadInput)
	}
	s.mu.Lock()
	defer s.mu.Unlock()
	now := time.Now().UTC().Format(time.RFC3339Nano)
	res, err := s.db.Exec(
		`UPDATE interaction SET end_req_by=?, status=?, updated_at=? WHERE id=? AND status IN ('queued','ending')`,
		by, string(StatusEnding), now, id)
	if err != nil {
		return err
	}
	if n, _ := res.RowsAffected(); n == 0 {
		return ErrNotFound
	}
	return nil
}

